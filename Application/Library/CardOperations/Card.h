// 功    能：   IC卡测试
// 入口参数：   无
// 出口参数：   无
//---------------------------------------------------------------------
extern	uint8_t   TestICCard(void);

//---------------------------------------------------------------------
// 名    称：   uint32_t GetToEndLen(uint32_t Addr)
// 功    能：   指定到指定地址到卡的结束的长度
// 入口参数：   Addr    指定地址
